namespace Known;

/// <summary>
/// 代码配置信息类。
/// </summary>
public class CodeConfigInfo
{
    /// <summary>
    /// 取得或设置实体类所在项目路径。
    /// </summary>
    public string EntityPath { get; set; }

    /// <summary>
    /// 取得或设置页面类所在项目路径。
    /// </summary>
    public string PagePath { get; set; }

    /// <summary>
    /// 取得或设置服务实现类所在项目路径。
    /// </summary>
    public string ServicePath { get; set; }
}